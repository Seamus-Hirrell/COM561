/**
 * Program: EssentialData
 * Purpose: Simple demonstration of Java data representation.
 */
class EssentialData {

  public static void main(String args[]) {
    int myInteger = 42;
    double myReal = 3.14159;
    String someName = "Mr. Kyle";
    boolean myTruth = true;
    int myArray[] = { 1856, 1984 };
    System.out.println(myInteger);
    System.out.println(myReal);
    System.out.println(someName);
    System.out.println(myTruth);
    System.out.println(myArray[0]);
    System.out.println(myArray[1]);
    System.out.println(args[0]);
    System.out.println(args[1]);
    System.out.println(args[2]);
  } // end main
} // end EssentialData
